    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $class = Classe::find($id);
        $class->delete();

        return redirect('/classes')->with('success', ' Clasa a fost stearsa!');
