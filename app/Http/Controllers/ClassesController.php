    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //create class
        $class = new Classe;
        $class->name = $request->input('clasa');
        $class->grade = $request->input('an');
        $class->start_date = $request->input('data_start');
        $class->end_date = $request->input('data_final');
        $class->profil = $request->input('profil');
        $class->save();

        return redirect('classes')->with('success', 'Clasa a fost adaugata');
    }

